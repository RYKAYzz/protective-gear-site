// Get all achievements
AchievementDatabase.getAll = () => {
  return Object.values(AchievementDatabase).filter(Array.isArray).flat();
};

// Get achievements by category
AchievementDatabase.getByCategory = (category) => {
  return AchievementDatabase[category] || [];
};

// Get random achievements
AchievementDatabase.getRandom = (count = 10) => {
  const all = AchievementDatabase.getAll();
  const shuffled = all.sort(() => 0.5 - Math.random());
  return shuffled.slice(0, count);
};

// Get achievements by rarity
AchievementDatabase.getByRarity = (rarity) => {
  const all = AchievementDatabase.getAll();
  return all.filter((achievement) => achievement.rarity === rarity);
};

// Export for use in other files
window.AchievementDatabase = AchievementDatabase;
